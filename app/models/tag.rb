class Tag < ActiveRecord::Base

  has_many :restaurant_tags
  has_many :restaurants, :through => :restaurant_tags

  def self.to_csv
    CSV.generate do |csv|
      csv << column_names
      all.each do |product|
        csv << product.attributes.values_at(*column_names)
      end
    end
  end
end
